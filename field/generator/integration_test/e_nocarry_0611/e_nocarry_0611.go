		C = madd0(m, qe_nocarry_0611[0], t[i+0])
		C, t[i+1] = madd2(m, qe_nocarry_0611[1], t[i+1], C)
		C, t[i+2] = madd2(m, qe_nocarry_0611[2], t[i+2], C)
		C, t[i+3] = madd2(m, qe_nocarry_0611[3], t[i+3], C)
		C, t[i+4] = madd2(m, qe_nocarry_0611[4], t[i+4], C)
