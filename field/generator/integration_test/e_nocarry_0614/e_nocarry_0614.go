// Exp z = x^exponent mod q
func (z *e_nocarry_0614) Exp(x e_nocarry_0614, exponent *big.Int) *e_nocarry_0614 {
	var bZero big.Int
	if exponent.Cmp(&bZero) == 0 {
		return z.SetOne()
	}

	z.Set(&x)

	for i := exponent.BitLen() - 2; i >= 0; i-- {
		z.Square(z)
		if exponent.Bit(i) == 1 {
			z.Mul(z, &x)
		}
	}

	return z
}

