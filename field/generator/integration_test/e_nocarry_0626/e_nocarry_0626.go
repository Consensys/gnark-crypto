// Sqrt z = x mod q
// if the square root doesn't exist (x is not a square mod q)
// Sqrt leaves z unchanged and returns nil
func (z *e_nocarry_0626) Sqrt(x *e_nocarry_0626) *e_nocarry_0626 {
	// q  1 (mod 4)
	// see modSqrtTonelliShanks in math/big/int.go
	// using https://www.maa.org/sites/default/files/pdf/upload_library/22/Polya/07468342.di020786.02p0470a.pdf

	var y, b, t, w e_nocarry_0626
	// w = x^((s-1)/2))
	w.Exp(*x, _bSqrtExponente_nocarry_0626)

	// y = x^((s+1)/2)) = w * x
	y.Mul(x, &w)

	// b = x^s = w * w * x = y * x
	b.Mul(&w, &y)

	// g = nonResidue ^ s
	var g = e_nocarry_0626{
		10572035249369930312,
		15110731073105787147,
		18324199482059050044,
		8928521664203262135,
		17361051150316770485,
		16472810843697390969,
		3824954252456352456,
		8832573348608147097,
		6923782304542042039,
		109177356969835,
	}
	r := uint64(4)

	// compute legendre symbol
	// t = x^((q-1)/2) = r-1 squaring of x^s
	t = b
	for i := uint64(0); i < r-1; i++ {
		t.Square(&t)
	}
	if t.IsZero() {
		return z.SetZero()
	}
	if !((t[9] == 324715957562151) && (t[8] == 12566152193423437880) && (t[7] == 15528585030219062836) && (t[6] == 15427548309077225266) && (t[5] == 6913455460967751197) && (t[4] == 17943336572420034070) && (t[3] == 16589964505397812886) && (t[2] == 17951967516837406929) && (t[1] == 11490927834382642522) && (t[0] == 7341744560860109981)) {
		// t != 1, we don't have a square root
		return nil
	}
	for {
		var m uint64
		t = b

		// for t != 1
		for !((t[9] == 324715957562151) && (t[8] == 12566152193423437880) && (t[7] == 15528585030219062836) && (t[6] == 15427548309077225266) && (t[5] == 6913455460967751197) && (t[4] == 17943336572420034070) && (t[3] == 16589964505397812886) && (t[2] == 17951967516837406929) && (t[1] == 11490927834382642522) && (t[0] == 7341744560860109981)) {
			t.Square(&t)
			m++
		}

		if m == 0 {
			return z.Set(&y)
		}
		// t = g^(2^(r-m-1)) mod q
		ge := int(r - m - 1)
		t = g
		for ge > 0 {
			t.Square(&t)
			ge--
		}

		g.Square(&t)
		y.Mul(&y, &t)
		b.Mul(&b, &g)
		r = m
	}
}

