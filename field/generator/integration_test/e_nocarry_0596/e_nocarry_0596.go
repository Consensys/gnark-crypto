				s[5], carry = bits.Add64(s[5], 7892006461675299013, carry)
				s[6], carry = bits.Add64(s[6], 12172765359602928379, carry)
				s[7], carry = bits.Add64(s[7], 8892457057294729852, carry)
				s[8], carry = bits.Add64(s[8], 9858084393982308949, carry)
