			u[6], borrow = bits.Sub64(u[6], v[6], borrow)
			u[7], borrow = bits.Sub64(u[7], v[7], borrow)
			u[8], borrow = bits.Sub64(u[8], v[8], borrow)
			u[9], _ = bits.Sub64(u[9], v[9], borrow)

			// r = r - s
			r[0], borrow = bits.Sub64(r[0], s[0], 0)
