		m = t[i] * qInvNegLsw

		//TODO: Is it better to hard-code the values of qe_nocarry_0596 as the "reduce" template does?
		C = madd0(m, qe_nocarry_0596[0], t[i+0])
		C, t[i+1] = madd2(m, qe_nocarry_0596[1], t[i+1], C)
		C, t[i+2] = madd2(m, qe_nocarry_0596[2], t[i+2], C)
		C, t[i+3] = madd2(m, qe_nocarry_0596[3], t[i+3], C)
		C, t[i+4] = madd2(m, qe_nocarry_0596[4], t[i+4], C)
		C, t[i+5] = madd2(m, qe_nocarry_0596[5], t[i+5], C)
